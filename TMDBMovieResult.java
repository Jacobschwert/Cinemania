public class TMDBMovieResult {
    private String overview;
    private int[] genre_ids;
    private int id;
    private String title;
    
    public String getOverview() {
        return overview;
    }

    public int[] getGenre_ids() {
        return genre_ids;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }
}
